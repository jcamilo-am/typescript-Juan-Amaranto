//  types of variables

//  string
const username: string = 'Juan Camilo';
console.log(username, 'username');

//  number
const age: number = 30;
console.log(age, 'age');

//  boolean
const esEstudiante: boolean = true;
console.log(esEstudiante, 'esEstudiante');

//  any
let data: any = 'data';
data = 10;
console.log(data, 'data');

//  symbol
const symbol: symbol = Symbol('mySymbol');
console.log(symbol, 'symbol');
